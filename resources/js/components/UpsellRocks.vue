      if (!this.loaded && window.sessionToken != null) {
        this.loaded = true;
        this.getUpsells();
        this.getUser();
        this.getShop();
        this.getCurrencies();
        this.getCustomCollections();
        this.getSmartCollections();
        this.getLocalProducts();
        this.getLocalCollections();
        this.updateSmartCollection();
        clearInterval(this.interval);
      }
