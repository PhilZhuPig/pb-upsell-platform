      setting: state => state.upsellrock.setting
    })
  },
  mounted() {
    console.log("setting mounted");
    this.getSetting();
  },
  methods: {
    getSetting() {
      axios.get("/spa/setting").then(res => {
        this.$store.commit("upsellrock/SET_SETTING", { setting: res.data });
        if (this.setting.max_popup_session_views > 0) {
          this.unlimited = false;
        }
      });
    },
    updateSetting() {
      let loader = this.$loading.show({
        // Optional parameters
        container: this.fullPage ? null : this.$refs.formContainer,
        canCancel: true,
        onCancel: this.onCancel
      });
      if (this.unlimited === true) {
        var newSetting = Object.clone(this.setting);
        newSetting.max_popup_session_views = 0;
        this.$store.commit("upsellrock/SET_SETTING", { setting: newSetting });
      }
      axios
        .put("/spa/setting", this.setting)
        .then(res => {
          this.$store.commit("upsellrock/SET_SETTING", { setting: res.data });
          loader.hide();
          this.updated_success = true;
          setTimeout(() => {
            this.updated_success = false;
          }, 2000);
        })
        .catch(error => {
          loader.hide();
        });
    },
    gotoCCA() {
      window
        .open("https://apps.shopify.com/currency-converter-ant", "_blank")
        .focus();
    }
  }
};
</script>
